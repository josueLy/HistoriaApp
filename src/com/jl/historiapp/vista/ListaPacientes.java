    public ListaPacientes(CRUDHistoria crudHistoria) {
        this.crudHistoria = crudHistoria;
        initComponents();
        modelo = (DefaultTableModel) tblPacientes.getModel();
        Object[] rowData = new Object[6];
        List<Paciente> pacientes = pacienteControlador.listarPacientes();

        for (int i = 0; i < pacientes.size(); i++) {
            rowData[0] = pacientes.get(i).getDni();
            rowData[1] = pacientes.get(i).getNombre();
            rowData[2] = pacientes.get(i).getEdad();
            rowData[3] = pacientes.get(i).getSexo();
            rowData[4] = pacientes.get(i).getEstaoCivil();
            rowData[5] = pacientes.get(i).getId();
            modelo.addRow(rowData);
        }
        tblPacientes.getColumnModel().getColumn(5).setMinWidth(0);
        tblPacientes.getColumnModel().getColumn(5).setMaxWidth(0);
        tblPacientes.getColumnModel().getColumn(5).setWidth(0);
    }

