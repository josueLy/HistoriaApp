    private void txtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyPressed

        codigoHistoria =txtCodigo.getText();
        List<Paciente> pacientes = pacienteControlador.buscarPacientePorNombre(codigoHistoria);

        modelo = (DefaultTableModel) this.tblPacientes.getModel();
        modelo.setRowCount(0);
        Object[] filadata = new Object[6];
        for (Paciente paciente : pacientes) {
            filadata[0] = paciente.getDni();
            filadata[1] = paciente.getNombre();
            filadata[2] = paciente.getEdad();
            filadata[3] = paciente.getSexo();
            filadata[4] = paciente.getEstaoCivil();
            filadata[5] = paciente.getId();
            
            modelo.addRow(filadata);
        }
    }//GEN-LAST:event_txtCodigoKeyPressed

