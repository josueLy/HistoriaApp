    private void dchHastaPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_dchHastaPropertyChange
       
        Date fechaDesde = dchDesde.getDate();
        Date fechaHasta = dchHasta.getDate();
        if (dchDesde.getDate() != null && dchHasta.getDate() != null) {
            List<Evolucion> evoluciones = evolucionControlador.buscarRecetaPorFecha(fechaDesde, fechaHasta, this.crudHistoria.historia);
            modelo = (DefaultTableModel) this.tblEvoluciones.getModel();
            modelo.setRowCount(0);
            Object[] filadata = new Object[3];
            for (Evolucion evolucion : evoluciones) {
                filadata[0] = evolucion.getId();
                filadata[1] = evolucion.getDescripcion();
                filadata[2] = evolucion.getFecha();
                modelo.addRow(filadata);
            }
        }
    }//GEN-LAST:event_dchHastaPropertyChange

