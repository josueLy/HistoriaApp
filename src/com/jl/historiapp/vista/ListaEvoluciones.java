    public ListaEvoluciones(CRUDHistoria crudHistoria) {
        initComponents();
        this.crudHistoria = crudHistoria;
        modelo = (DefaultTableModel) tblEvoluciones.getModel();
        Object[] rowData = new Object[6];
        List<Evolucion> evoluciones = evolucionControlador.listarEvoluciones(crudHistoria.historia);

        for (int i = 0; i < evoluciones.size(); i++) {
            rowData[0] = evoluciones.get(i).getId();
            rowData[1] = evoluciones.get(i).getDescripcion();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");            
            rowData[2] = formato.format(evoluciones.get(i).getFecha());

            modelo.addRow(rowData);
        }
        tblEvoluciones.getColumnModel().getColumn(0).setMinWidth(0);
        tblEvoluciones.getColumnModel().getColumn(0).setMaxWidth(0);
        tblEvoluciones.getColumnModel().getColumn(0).setWidth(0);
    }

