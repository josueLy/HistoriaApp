    public void llenarDatosHistoriaPorPaciente(int idPaciente) {

        paciente = pacienteControlador.mostrarDatosPaciente(idPaciente);
        txtDNI.setText(paciente.getDni());
        txtNombre.setText(paciente.getNombre());
        txtEdad.setText(paciente.getEdad().toString());
        cmbSexo.setSelectedItem(paciente.getSexo());
        cmbEstadoCivil.setSelectedItem(paciente.getEstaoCivil());
        historia = historiaControlador.mostrarDatosHistoriaPorPaciente(paciente);
        txaMotivo.setText(historia.getMotivo());
        txaEnfermedad.setText(historia.getEnfermedadActual());
        txaDiagnostico.setText(historia.getDiagnostico());
        evolucion = evolucionControlador.mostrarUltimaEvolucion(historia);
        txaEvolucion.setText(evolucion.getDescripcion());
        receta = recetaControlador.mostrarUltimaReceta(historia);
        txaReceta.setText(receta.getMedicinas());
        btnImprimir.setEnabled(true);
    }

    public void llenarEvolucion(int idEvolucion) {
        evolution = evolucionControlador.obtenerEvolucionPorId(idEvolucion);
        txaEvolucion.setText(evolution.getDescripcion());
    }

    public void llenarReceta(int idReceta) {
        receta = recetaControlador.mostrarRecetaPorId(idReceta);
        txaReceta.setText("");
        txaReceta.append(receta.getMedicinas());
    }

